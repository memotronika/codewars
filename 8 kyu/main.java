// Simple multiplication
public class Sid {
    public static int simpleMultiplication(int n) {
        //your code here
        return (n%2==0) ? n*8:n*9;
    }
}

// Even or Odd
public class EvenOrOdd {
    public static String even_or_odd(int number) {
        if (number%2==0){return "Even";}
        else{return "Odd";}
        
    }
}

